#Half Pyramid Pattern of Number
#number_half_pyramid

rows = 5

#Outer loop controls the rows
for i in range(1, rows + 1):
    
    #Inner loop prints numbers from 1 to i
    for j in range(1, i + 1):       
        
        #Display the numbers on the same line      
          print(j, end=" ")
        
    #New line after each row       
    print()
                   
    
   
   

    